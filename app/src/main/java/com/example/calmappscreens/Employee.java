package com.example.calmappscreens;

public class Employee {

    private Integer _empid;
    private String _name;
    private String _manager;
    private String _dept;
    private String _num;
    private String _mnum;
    private String _res;
    private String _mess;
    String _loc;

    public Employee() {
    }

    public Integer get_empid() {
        return _empid;
    }

    public void set_empid(Integer _empid) {
        this._empid = _empid;
    }

    public String get_name() {
        return _name;
    }

    public void set_name(String _name) {
        this._name = _name;
    }

    public String get_manager() {
        return _manager;
    }

    public void set_manager(String _manager) {
        this._manager = _manager;
    }

    public String get_dept() {
        return _dept;
    }

    public void set_dept(String _dept) {
        this._dept = _dept;
    }

    public String get_num() {
        return _num;
    }

    public void set_num(String _num) {
        this._num = _num;
    }

    public String get_mnum() {
        return _mnum;
    }

    public void set_mnum(String _mnum) {
        this._mnum = _mnum;
    }

    public String get_res() {
        return _res;
    }

    public void set_res(String _res) {
        this._res = _res;
    }

    public String get_mess() {
        return _mess;
    }

    public void set_mess(String _message) {
        this._mess = _message;
    }

    public String get_loc() {
        return _loc;
    }

    public void set_loc(String _loc) {
        this._loc = _loc;
    }
}
